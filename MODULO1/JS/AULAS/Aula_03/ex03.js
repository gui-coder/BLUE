  
/*O método da classe String que transforma um texto em caixa alta é o toUpperCase().*/

const frase = "Este texto ficará maiúsculo";

console.log(frase.toUpperCase());

/*Podemos fazer o contrário também, ou seja, tornar um texto recebido totalmente em caixa baixa. Para isso, contamos com o auxílio do método toLowerCase().*/

let texto = "ESTE TEXTO SERÁ TRANSFORMADO EM CAIXA BAIXA";
console.log(texto.toLowerCase());